def get_duration_seconds(src: str) -> float | None:
    try:
        p = subprocess.run(
            [
                "ffprobe",
                "-v", "error",
                "-show_entries", "format=duration",
                "-of", "default=noprint_wrappers=1:nokey=1",
                src,
            ],
            capture_output=True,
            text=True,
        )
        return float(p.stdout.strip())
    except Exception as exc:
        logger.warning("get_duration_seconds(%s) failed: %s", src, exc)
        return None


def get_fps(src: str) -> float | None:
    try:
        p = subprocess.run(
            [
                "ffprobe",
                "-v", "error",
                "-select_streams", "v:0",
                "-show_entries", "stream=r_frame_rate",
                "-of", "default=noprint_wrappers=1:nokey=1",
                src,
            ],
            capture_output=True,
            text=True,
        )
        val = p.stdout.strip()
        if "/" in val:
            num, den = val.split("/", 1)
            return float(num) / float(den)
        return float(val)
    except Exception as exc:
        logger.warning("get_fps(%s) failed: %s", src, exc)
        return None


def probe_streams(src: str) -> list:
    try:
        p = subprocess.run(
            [
                "ffprobe",
                "-v", "error",
                "-print_format", "json",
                "-show_streams",
                src,
            ],
            capture_output=True,
            text=True,
        )
        data = json.loads(p.stdout)
        return data.get("streams", [])
    except Exception as exc:
        logger.warning("probe_streams(%s) failed: %s", src, exc)
        return []


def codecs_for_profile(profile: str) -> str:
    v = {
        "baseline": "avc1.42E01E",
        "main": "avc1.4D401F",
        "high": "avc1.640028",
    }.get(profile, "avc1.640028")
    return f'{v},mp4a.40.2'


def width_from_height(h: int) -> int:
    return int(math.floor(h * 16 / 9 / 2) * 2)


def count_segments(path: str) -> int:
    total = 0
    for root, _, files in os.walk(path):
        for f in files:
            if f.endswith(".m4s"):
                total += 1
    return total


def create_poster(src: str, outdir: str):
    subprocess.run(
        [
            "ffmpeg", "-y",
            "-i", src,
            "-ss", "00:00:00",
            "-vframes", "1",
            "-update", "1",
            "-vf", "scale=1280:-2",
            os.path.join(outdir, "poster.jpg"),
        ],
        check=True,
    )


def create_preview_thumbnails(src: str, outdir: str, duration: float, count: int = 6):
    """Generate small preview thumbnails at evenly spaced intervals."""
    if not duration or duration < 2:
        return
    positions = [duration * p for p in [0.10, 0.25, 0.40, 0.55, 0.70, 0.85]]
    previews = []
    for i, t in enumerate(positions[:count]):
        ts = f"{int(t // 3600):02d}:{int(t % 3600 // 60):02d}:{t % 60:06.3f}"
        fname = f"preview_{i}.jpg"
        try:
            subprocess.run(
                [
                    "ffmpeg", "-y",
                    "-ss", ts,
                    "-i", src,
                    "-vframes", "1",
                    "-update", "1",
                    "-vf", "scale=320:-2",
                    os.path.join(outdir, fname),
                ],
                check=True,
                capture_output=True,
            )
            previews.append({"t": round(t, 2), "f": fname})
        except Exception as exc:
            logger.warning("Preview thumbnail %d failed at %.1fs: %s", i, t, exc)

    if previews:
        with open(os.path.join(outdir, "previews.json"), "w") as f:
            json.dump(previews, f)
        logger.info("Generated %d preview thumbnails in %s", len(previews), outdir)

    # Large previews for embed player
    previews_lg = []
    for i, t in enumerate(positions[:count]):
        ts = f"{int(t // 3600):02d}:{int(t % 3600 // 60):02d}:{t % 60:06.3f}"
        fname = f"preview_lg_{i}.jpg"
        try:
            subprocess.run(
                [
                    "ffmpeg", "-y",
                    "-ss", ts,
                    "-i", src,
                    "-vframes", "1",
                    "-update", "1",
                    "-vf", "scale=960:-2",
                    os.path.join(outdir, fname),
                ],
                check=True,
                capture_output=True,
            )
            previews_lg.append({"t": round(t, 2), "f": fname})
        except Exception as exc:
            logger.warning("Large preview thumbnail %d failed at %.1fs: %s", i, t, exc)

    if previews_lg:
        with open(os.path.join(outdir, "previews_lg.json"), "w") as f:
            json.dump(previews_lg, f)
        logger.info("Generated %d large preview thumbnails in %s", len(previews_lg), outdir)


