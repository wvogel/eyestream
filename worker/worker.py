def make_conninfo() -> str:
    return (
        f"host={DB_HOST} port={DB_PORT} dbname={DB_NAME} "
        f"user={DB_USER} password={DB_PASSWORD}"
    )


def wait_for_db(max_attempts: int = 30, delay_seconds: int = 2):
    for attempt in range(max_attempts):
        try:
            with psycopg.connect(make_conninfo()):
                return
        except Exception as exc:
            logger.warning("DB not ready (attempt %d/%d): %s", attempt + 1, max_attempts, exc)
            time.sleep(delay_seconds)
    raise RuntimeError("Could not connect to database after %d attempts" % max_attempts)


def migrate_timestamp_columns(conn):
    """Migrate TEXT timestamp columns to TIMESTAMPTZ (idempotent)."""
    row = conn.execute(
        """
        SELECT data_type FROM information_schema.columns
        WHERE table_name='videos' AND column_name='uploaded_at'
        """
    ).fetchone()

    if not row or row["data_type"] != "text":
        return

    logger.info("Migrating timestamp columns from TEXT to TIMESTAMPTZ...")
    for col in ("uploaded_at", "started_at", "encoded_at", "queued_at"):
        conn.execute(
            f"ALTER TABLE videos ALTER COLUMN {col} "
            f"TYPE TIMESTAMPTZ USING NULLIF({col}, '')::timestamptz"
        )
    logger.info("Timestamp migration complete.")


def ensure_schema(conn):
    conn.execute(
        """
        CREATE TABLE IF NOT EXISTS videos (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            orig_name TEXT,
            stored_name TEXT,
            uploaded_at TIMESTAMPTZ,
            owner_email TEXT,
            status TEXT,
            progress INTEGER DEFAULT 0,
            playlist_url TEXT,
            poster_url TEXT,
            error TEXT,
            started_at TIMESTAMPTZ,
            ffmpeg_pid INTEGER,
            cancel_requested INTEGER DEFAULT 0,
            eta_seconds INTEGER,
            encoded_at TIMESTAMPTZ,
            duration_seconds DOUBLE PRECISION,
            streams_json TEXT,
            queued_at TIMESTAMPTZ,
            note TEXT DEFAULT ''
        )
        """
    )
    conn.execute(
        """
        CREATE TABLE IF NOT EXISTS worker_heartbeat (
            id INTEGER PRIMARY KEY DEFAULT 1,
            last_seen TIMESTAMPTZ,
            status TEXT,
            cpu_percent INTEGER
        )
        """
    )
    conn.execute(
        "INSERT INTO worker_heartbeat (id, last_seen, status) VALUES (1, now(), 'idle') ON CONFLICT DO NOTHING"
    )
    # Migrate: add cpu_percent column if missing
    col = conn.execute(
        "SELECT column_name FROM information_schema.columns WHERE table_name='worker_heartbeat' AND column_name='cpu_percent'"
    ).fetchone()
    if not col:
        conn.execute("ALTER TABLE worker_heartbeat ADD COLUMN cpu_percent INTEGER")
    conn.execute(
        """
        CREATE INDEX IF NOT EXISTS idx_videos_status_queued_at_id
        ON videos (status, queued_at, id)
        """
    )
    try:
        conn.execute("CREATE EXTENSION IF NOT EXISTS pg_trgm")
        conn.execute(
            """
            CREATE INDEX IF NOT EXISTS idx_videos_orig_name_trgm
            ON videos USING GIN (orig_name gin_trgm_ops)
            """
        )
        conn.execute(
            """
            CREATE INDEX IF NOT EXISTS idx_videos_note_trgm
            ON videos USING GIN (note gin_trgm_ops)
            """
        )
    except Exception as exc:
        logger.warning("pg_trgm extension not available, trigram indexes skipped: %s", exc)

    migrate_timestamp_columns(conn)


def reset_encoding_jobs(pool: ConnectionPool):
    with pool.connection() as c:
        c.execute(
            """
            UPDATE videos
            SET status='queued',
                progress=0,
                eta_seconds=NULL,
                error='Encoding durch Neustart unterbrochen',
                started_at=NULL,
                ffmpeg_pid=NULL,
                cancel_requested=0
            WHERE status='encoding'
            """
        )


def cleanup_tmp_dirs():
    if not os.path.exists(HLS_DIR):
        return
    for name in os.listdir(HLS_DIR):
        if name.startswith(".tmp_"):
            shutil.rmtree(os.path.join(HLS_DIR, name), ignore_errors=True)


